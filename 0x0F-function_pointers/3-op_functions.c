#include "3-calc.h"


/**
 * op_add - adds integers
 * @a: first argument
 * @b: second argument
 *
 * Return: Add
 */

int op_add(int a, int b)
{
       return (a + b); 
}



/**
 * op_sub - subs integers
 * @a: first argument
 * @b: second argument
 *
 * Return : sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies integers
 * @a: first argument
 * @b: second argument
 *
 * Return : mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * op_div - divides integers
 * @a: first argument
 * @b: second argument
 *
 * Return : div
 */

int op_div(int a, int b)
{
	return (a / b);
}



/**
 * op_mod - mods integers
 * @a: first argument
 * @b: second argument
 *
 * Return : mod
 */
int op_mod(int a, int b)
{
    return (a % b);
}
